import UIKit

extension UIFont {
    /// body/regular fonts
    static let bodyS = UIFont.systemFont(ofSize: 14, weight: .regular)
    static let bodyM = UIFont.systemFont(ofSize: 16, weight: .regular)
    static let bodyL = UIFont.systemFont(ofSize: 18, weight: .regular)
    
    /// headLine fonts
    static let headLineS = UIFont.systemFont(ofSize: 16, weight: .bold)
    static let headLineM = UIFont.systemFont(ofSize: 18, weight: .bold)
    static let headLineL = UIFont.systemFont(ofSize: 20, weight: .bold)
}
